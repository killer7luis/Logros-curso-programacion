# 1. Calculadora de Área de un Triángulo: Escribe un programa que pida al usuario la base
# y la altura de un triángulo. El programa debe calcular y mostrar el área.
# Recuerda que la fórmula es (base * altura) / 2

base = int(input("Coloque la base"))
altura = int(input("Coloque la altura"))

Calculo = base * altura / 2

print("El area del triangulo es",Calculo)